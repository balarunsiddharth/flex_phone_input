/// ISO codes pinned to the top of the selector by default (the "popular" group).
/// Override per-widget with `favoriteCountries`.
const List<String> kDefaultFavoriteIso = <String>['IN', 'US', 'GB', 'AE', 'SG', 'AU'];

/// Looks up a country by [iso] (ISO 3166-1 alpha-2, case-insensitive).
///
/// Searches [source] (defaults to [kCountries]) and returns `null` if absent.
Country? findCountryByIso(String iso, {List<Country> source = kCountries}) {
  final String code = iso.toUpperCase();
  for (final Country c in source) {
    if (c.isoCode == code) return c;
  }
  return null;
}

/// Looks up the first country matching a [dialCode] (without `+`).
///
/// Note: some calling codes are shared (e.g. `1`, `7`), so this returns the
/// first match in [source]; prefer storing a [Country] over inferring one.
Country? findCountryByDialCode(String dialCode, {List<Country> source = kCountries}) {
  final String d = dialCode.replaceAll('+', '').trim();
  for (final Country c in source) {
    if (c.dialCode == d) return c;
  }
  return null;
}
